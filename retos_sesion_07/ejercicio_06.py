
cadena = "hola mundo"
cadena_mayusculas = cadena.upper()
print("Ejemplo 1 - Función upper:")
print(f"Original: {cadena}, Mayúsculas: {cadena_mayusculas}")

cadena_minusculas = cadena_mayusculas.lower()
print("Ejemplo 2 - Función lower:")
print(f"Original: {cadena_mayusculas}, Minúsculas: {cadena_minusculas}")

cadena_titulo = cadena.title()
print("Ejemplo 3 - Función title:")
print(f"Original: {cadena}, Título: {cadena_titulo}")